public class Variable05 {
    public static void main(String[] args) {
        //deklarasi var
        //isi dari variable
        String NIM = "2311016210015";
        String nama = "Muhammad Najib Akbar"; 

        //command tulis/print
        System.out.println("NIM: " + NIM);
        System.out.println("Nama: " + nama);
        
        System.out.println("==================="); //spasi aesthetic 

        //Ganti isi variable
        NIM = "2311016210020";
        nama = "Muhammad Naufal Alwy"; 
        //reprint
        System.out.println("NIM: " + NIM);
        System.out.println("Nama: " + nama);
    }
}
